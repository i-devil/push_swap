#include "push_swap.h"

int			ft_atoi(const char *str)
{
	int		i;
	int		a;
	int		c;

	i = 0;
	c = 0;
	a = 0;
	while ((str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
		str[i] == '\r' || str[i] == '\v' || str[i] == '\f') && c == 0)
		i++;
	if (str[i] == '-' && c == 0)
	{
		c = -1;
		i++;
	}
	if (str[i] == '+' && c == 0)
	{
		c = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
		a = (a * 10) + (str[i++] - 48);
	if (c != 0)
		a = c * a;
	return (a);
}
